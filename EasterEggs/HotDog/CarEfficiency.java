import java.util.Scanner;

    public class CarEfficiency{

    public static void main(String args []){

    Scanner UserInput = new Scanner(System.in);

    System.out.print("Enter the distance to drive: ");

    int DistanceToDrive = UserInput.nextInt();

    System.out.print("Enter efficiency of the car: ");

    int CarEfficiency = UserInput.nextInt();

    System.out.print("Enter price per gallon: ");

    int PricePerGallon = UserInput.nextInt();

    double Total = (DistanceToDrive / CarEfficiency) * PricePerGallon;

    System.out.println("Total cost of the trip is: " + Total);


}
}







































































































































































































































































































































































































































































































































































